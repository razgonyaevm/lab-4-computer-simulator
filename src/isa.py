"""Модуль описания системы команд (ISA), регистров и режимов адресации.

Определяет базовые константы процессора, такие как размер памяти, порты ввода-вывода,
а также перечисления для опкодов, регистров общего и специального назначения.
"""

from enum import IntEnum

# Размеры памяти
DATA_MEMORY_SIZE = 4096
INSTRUCTION_MEMORY_SIZE = 4096
VECTOR_SIZE = 4  # Количество элементов в векторном регистре

# Константы для Memory-mapped I/O
MMIO_INPUT = 0xFF00
MMIO_OUTPUT = 0xFF01


class OpCode(IntEnum):
    """Перечисление кодов операций (Opcodes) процессора.

    Каждая инструкция кодируется 1 байтом. Поддерживает скалярную арифметику,
    управление потоком команд, прерывания (Trap) и векторные операции (Vector).
    """

    # Управление данными (CISC: могут работать и с регистрами, и с памятью)
    MOV = 0x01  # MOV DST, SRC
    LOAD = 0x02  # LOAD REG, [ADDR]
    STORE = 0x03  # STORE [ADDR], REG
    PUSH = 0x04  # PUSH SRC
    POP = 0x05  # POP DST

    # Арифметика (Скалярная)
    ADD = 0x10
    SUB = 0x11
    MUL = 0x12
    DIV = 0x13
    CMP = 0x14

    # Векторные операции (Ваш вариант: Vector)
    VLOAD = 0x20  # Загрузить вектор из памяти в V-регистр
    VSTORE = 0x21  # Сохранить V-регистр в память
    VADD = 0x22  # Поэлементное сложение векторов
    VSUB = 0x23  # Поэлементное вычитание векторов
    VMUL = 0x24  # Поэлементное умножение векторов
    VDIV = 0x25  # Поэлементное деление векторов
    VCMP = 0x26  # Поэлементное сравнение векторов

    # Управление потоком (CISC: переходы)
    JMP = 0x30
    JZ = 0x31  # Jump if Zero
    JNZ = 0x32  # Jump if Not Zero
    CALL = 0x33  # Вызов функции (сохраняет PC и FP на стек)
    RET = 0x34  # Возврат из функции
    JL = 0x35  # Jump if less (результат CMP < 0)

    # Прерывания (Ваш вариант: Trap)
    INT = 0x40  # Вызов программного прерывания
    IRET = 0x41  # Возврат из обработчика прерывания

    HALT = 0xFF


class Register(IntEnum):
    """Перечисление регистров процессора.

    Включает 8 регистров общего назначения (R0-R7), 4 векторных регистра (V0-V3),
    а также служебные регистры: PC (счетчик команд), SP (указатель стека),
    FP (указатель кадра стека) и SR (регистр флагов состояния).
    """

    R0 = 0
    R1 = 1
    R2 = 2
    R3 = 3
    R4 = 4
    R5 = 5
    R6 = 6
    R7 = 7
    # Векторные регистры
    V0 = 8
    V1 = 9
    V2 = 10
    V3 = 11
    # Специальные
    PC = 12
    SP = 13  # Stack Pointer
    FP = 14  # Frame Pointer (необходим для Lisp-рекурсии)
    SR = 15  # Status Register (флаги Z, N, V, C)


class AddressingMode(IntEnum):
    """Режимы адресации операндов.

    Определяют способ интерпретации аргументов инструкции: работа напрямую
    с регистрами, константами (Immediate), прямой или косвенной памятью.
    """

    REGISTER = 0x01
    """Регистровая адресация.
    Операндом является значение, хранящееся непосредственно в регистре общего
    назначения (R0-R7) или специальном регистре. Обращения к ОЗУ данных не происходит.
    Пример: MOV R1, R0 (скопировать значение R0 в R1).
    """

    IMMEDIATE = 0x02
    """Непосредственная адресация.
    Операндом является константа (число), записанная непосредственно в памяти
    команд в качестве полезной нагрузки (Payload) инструкции.
    Пример: MOV R0, #5 (записать число 5 в регистр R0).
    """

    DIRECT = 0x03
    """Прямая адресация памяти.
    Адрес целевой ячейки памяти данных жестко зафиксирован в поле Payload инструкции.
    Пример: LOAD R0, [100] (прочитать значение из ячейки ОЗУ с адресом 100 в R0).
    """

    INDIRECT = 0x04
    """Косвенная регистровая адресация со смещением.
    Физический адрес ячейки памяти данных вычисляется динамически во время выполнения
    как сумма значения в базовом регистре (например, FP или SP) и смещения,
    переданного в поле Payload инструкции.
    Пример: LOAD R0, [FP + 8] (прочитать аргумент функции из стека).
    """
